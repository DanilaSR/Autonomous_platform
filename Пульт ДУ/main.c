struct Stack {

    uint16_t data[100];
    int capacity;
    int size;
};

int Stack_ctor_(struct Stack* stack) {
    stack->size     = 0;
    stack->capacity = 100;

    return 1;
}

int push(struct Stack* stack, uint16_t value, UART_HandleTypeDef *huart1) {

    char str[50];
		if (stack->size < stack->capacity) {
			stack->data[stack->size++] = value;
			
			sprintf(str, "%d", stack->size);
			HAL_UART_Transmit_IT(huart1, (uint8_t*) str, 50);
			HAL_Delay(100);
			
    }else{
			sprintf (str, "Stack Overflow!!!");
			HAL_UART_Transmit_IT(huart1, (uint8_t*) str, 50);
			HAL_Delay(100);
		}

    

		return 1;
}

int  pop(struct Stack* stack, UART_HandleTypeDef *huart1) {


    if (stack->size == 0) {
				char str[50] = {"Cannot pop from empty stack"};
				HAL_UART_Transmit_IT(huart1, (uint8_t*) str, 50);
				HAL_Delay(500);           
    }

    uint16_t del_element = stack->data[stack->size - 1];
    stack->data[--stack->size] = 0;
		HAL_Delay(100);
    return del_element;
}


void I2C_send(uint8_t data, uint8_t flags)
{
	uint8_t up = data & 0xF0;                 
	uint8_t lo = (data << 4) & 0xF0;         
	                                          
	uint8_t data_arr[4];
	data_arr[0] = up|flags|BACKLIGHT|PIN_EN; 
	data_arr[1] = up|flags|BACKLIGHT;         
	data_arr[2] = lo|flags|BACKLIGHT|PIN_EN;
	data_arr[3] = lo|flags|BACKLIGHT;

	HAL_I2C_Master_Transmit(&hi2c1, LCD_ADDR, data_arr, sizeof(data_arr), HAL_MAX_DELAY);
	HAL_Delay(LCD_DELAY_MS);
}

void LCD_SendString(char *str)
{

	while(*str) { 
		I2C_send((uint8_t)(*str), 1);              
        str++;                                      
    }
}

void lcd_put_cur(int row, int col)
{
    switch (row)
    {
        case 0:
            col |= 0x80;
            break;
        case 1:
            col |= 0xC0;
            break;
    }

    I2C_send(col, 0);
}

